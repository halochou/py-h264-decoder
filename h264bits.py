    def more_data(self):
        return self.bits.pos < self.bits.length

    def byte_aligned(self):
        return self.bits.pos % 8 == 0

    def more_rbsp_data(self):
        if not self.more_data():
            return False
        i = self.bits.length - 1
        while i >= 0:
            if self.bits[i] == True:
                if self.bits.pos == i:
                    return False
                else:
                    return True
            i -= 1

